export const detectRegion = () => {
  const lang = navigator.language || navigator.languages?.[0] || 'en-US'
  const timezone = Intl.DateTimeFormat().resolvedOptions().timeZone || ''

  if (timezone.includes('Asia/Kolkata') || timezone.includes('Asia/Calcutta')) return 'IN'
  if (lang.includes('en-IN') || lang.includes('hi')) return 'IN'
  if (lang.includes('en-GB') || timezone.includes('Europe/London')) return 'UK'
  return 'US'
}
